def configure_styles(doc):
    section = doc.sections[0]
    section.top_margin = Inches(1)
    section.bottom_margin = Inches(1)
    section.left_margin = Inches(1)
    section.right_margin = Inches(1)

    normal = doc.styles["Normal"]
    normal.font.name = "Calibri"
    normal.font.size = Pt(11)
    normal.paragraph_format.space_after = Pt(6)
    normal.paragraph_format.line_spacing = 1.25

    for style_name, size, color in [
        ("Heading 1", 16, "2E74B5"),
        ("Heading 2", 13, "2E74B5"),
        ("Heading 3", 12, "1F4D78"),
    ]:
        style = doc.styles[style_name]
        style.font.name = "Calibri"
        style.font.size = Pt(size)
        style.font.bold = True
        style.font.color.rgb = RGBColor.from_string(color)
        style.paragraph_format.space_before = Pt(14)
        style.paragraph_format.space_after = Pt(7)
        style.paragraph_format.line_spacing = 1.25

    bullet = doc.styles["List Bullet"]
    bullet.font.name = "Calibri"
    bullet.font.size = Pt(11)
    bullet.paragraph_format.left_indent = Inches(0.375)
    bullet.paragraph_format.first_line_indent = Inches(-0.188)
    bullet.paragraph_format.space_after = Pt(4)
    bullet.paragraph_format.line_spacing = 1.25


def set_cell_shading(cell, fill):
    tc_pr = cell._tc.get_or_add_tcPr()
    shd = OxmlElement("w:shd")
    shd.set(qn("w:fill"), fill)
    tc_pr.append(shd)


def set_cell_width(cell, width_dxa):
    tc_pr = cell._tc.get_or_add_tcPr()
    tc_w = tc_pr.first_child_found_in("w:tcW")
    if tc_w is None:
        tc_w = OxmlElement("w:tcW")
        tc_pr.append(tc_w)
    tc_w.set(qn("w:w"), str(width_dxa))
    tc_w.set(qn("w:type"), "dxa")


def add_metadata_table(doc, spec):
    table = doc.add_table(rows=3, cols=2)
    table.style = "Table Grid"
    widths = [1700, 7480]
    rows = [
        ("Назначение", spec["purpose"]),
        ("Основные файлы", "\n".join(spec["files"])),
        ("Владелец изменений", "При изменении указанных файлов обновлять этот документ и релевантные тесты."),
    ]

    for row_index, row_data in enumerate(rows):
        row = table.rows[row_index]
        for col_index, text in enumerate(row_data):
            cell = row.cells[col_index]
            set_cell_width(cell, widths[col_index])
            if col_index == 0:
                set_cell_shading(cell, "E8EEF5")
                for paragraph in cell.paragraphs:
                    for run in paragraph.runs:
                        run.bold = True
            cell.text = text
            for paragraph in cell.paragraphs:
                paragraph.paragraph_format.space_after = Pt(0)


def add_bullets(doc, items):
    for item in items:
        doc.add_paragraph(item, style="List Bullet")


def build_doc(spec):
    doc = Document()
    configure_styles(doc)

    title = doc.add_paragraph()
    title.alignment = WD_ALIGN_PARAGRAPH.LEFT
    title.paragraph_format.space_after = Pt(10)
    run = title.add_run(spec["title"])
    run.bold = True
    run.font.name = "Calibri"
    run.font.size = Pt(18)
    run.font.color.rgb = RGBColor.from_string("0B2545")

    doc.add_paragraph("Документация проекта OSRCGG. Язык: русский. Формат: compact_reference_guide.")

    doc.add_heading("Карта сущности", level=1)
    add_metadata_table(doc, spec)

    doc.add_heading("Ответственность", level=1)
    add_bullets(doc, spec["responsibilities"])

    doc.add_heading("Правила сопровождения", level=1)
    add_bullets(doc, spec["rules"])

    doc.add_heading("Что проверять при изменении", level=1)
    add_bullets(
        doc,
        [
            "Сохраняется ли прежняя внешняя схема файлов и пользовательских данных.",
            "Есть ли тест или ручная проверка для изменённой логики.",
            "Обновлена ли эта документация и память .agents, если меняется архитектурное правило.",
        ],
    )

    footer = doc.sections[0].footer.paragraphs[0]
    footer.alignment = WD_ALIGN_PARAGRAPH.CENTER
    footer.add_run("OSRCGG Architecture Documentation")

    out_path = OUT_DIR / spec["file"]
    doc.save(out_path)
    return out_path


def main():
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    created = [build_doc(spec) for spec in DOCS]
    for path in created:
        print(path)


if __name__ == "__main__":
    main()
